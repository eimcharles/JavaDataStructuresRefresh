package generics.BoundedVsWildcards;

public class Example {

    /**
     *          Bounded Type Parameters T VS Wildcards(?)
     *
     *          1. Bounded Type Parameters T:
     *
     *         - References to the type inside the method body.
     *         - Relates the return type to a parameter.
     *         - Can implement multiple Classes or Interfaces at once using & (Intersection).
     *         - Type parameters T do NOT support lower bounds.
     *
     *          2. Wildcards (?):
     *
     *         - Wildcards(?) can ONLY have ONE bounds.*
     *         - Wildcards(?) support upper bounds (ceiling).
     *         - Wildcards(?) support lower bounds (floor).
     *
     * */

}
